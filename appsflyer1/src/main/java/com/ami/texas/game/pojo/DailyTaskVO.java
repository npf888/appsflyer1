package com.ami.texas.game.pojo;

public class DailyTaskVO {

	private long taskId;
	private int finished;
	private int getNums;
	private int peopleNums;
	public int getFinished() {
		return finished;
	}
	public void setFinished(int finished) {
		this.finished = finished;
	}
	public int getGetNums() {
		return getNums;
	}
	public void setGetNums(int getNums) {
		this.getNums = getNums;
	}
	public long getTaskId() {
		return taskId;
	}
	public void setTaskId(long taskId) {
		this.taskId = taskId;
	}
	public int getPeopleNums() {
		return peopleNums;
	}
	public void setPeopleNums(int peopleNums) {
		this.peopleNums = peopleNums;
	}
	
	
	
}
